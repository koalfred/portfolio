        <h3>Planning Stage</h3>
        <p>MusicplayOnline is a website affiliated with Themes & Variations, a company based in Red Deer, Alberta, Canada that creates fun and interesting curriculum resources for elementary music teachers and students. The website contains resources such as lesson planning, themed units, interactives, games, thousands of videos and over 1,287 songs.</p>
        <p>This was another project where I worked on two different stages of the site, in collaboration with other developers of the team. Both stages involved development of MusicplayOnline's landing page based off of mockups provided through Adobe XD, and the company's vision of the site. Alike the development process of Musicplay.ca, I continuously made references to the mockups to ensure that the site is as pixel-perfect and user-friendly as possible, particularly in mobile, tablet and desktop view.</p>
        <p>This landing page project involved extensive use of WordPress/PHP, in which I used ACFs, such as tabs, WYSIWYG editor, text, text area, link and repeater fields. JavaScript was also used for dynamicity, in features such as the pricing field, sliders and modals, and SASS/CSS for giving the landing page the user-friendliness and colourful vibrance it has.</p>
